#ifndef GIE_PRIV_H_
#define GIE_PRIV_H_


/*****************************************************************************/
/*****************************************************************************/
/**                                                                         **/
/**                Address of general purpose register(SREG)                **/
/**                                                                         **/
/*****************************************************************************/
/*****************************************************************************/
#define SREG  				*((volatile u8*) 0x5f)
/*****************************************************************************/
/*****************************************************************************/
/*****************************************************************************/


/*****************************************************************************/
/*****************************************************************************/
/**                                                                         **/
/**             Definitions of global interrupt configuration               **/
/**                                                                         **/
/*****************************************************************************/
/*****************************************************************************/

#define ENABLE_GIE			SREG|=(1<<7)

#define DISABLE_GIE			SREG&=~(1<<7)

#define IF_GIE_DISABLE		!((SREG>>7) & 1)

#define IF_GIE_ENABLE		((SREG>>7) & 1)

/*****************************************************************************/
/*****************************************************************************/
/*****************************************************************************/




/*****************************************************************************/
/*****************************************************************************/
/**                                                                         **/
/**                 Defining of Interrupt Service Routine                   **/
/**                                                                         **/
/*****************************************************************************/
/*****************************************************************************/
#define ISR(VECT_NUM)	void VECT_NUM (void)__attribute__((signal,used));\
						void VECT_NUM (void)
/*****************************************************************************/
/*****************************************************************************/
/*****************************************************************************/





/*****************************************************************************/
/*****************************************************************************/
/**                                                                         **/
/**                        Address of NULL register                         **/
/**                                                                         **/
/*****************************************************************************/
/*****************************************************************************/

#define NULL  ((void*)0)

/*****************************************************************************/
/*****************************************************************************/
/*****************************************************************************/


#endif /* GIE_PRIV_H_ */
